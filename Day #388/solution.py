# Day 388: Open-ended: explain web cookies to a non-technical person. Prints the answer.

ANSWER = """Web cookies (explained simply):

A cookie is a small note that a website asks your browser to keep.
The next time you visit that site, your browser hands the note back, so
the site can remember you.

Think of a coat check at a theater: you hand over your coat and get a
numbered ticket. You keep the ticket (the cookie); when you return, you
show it and they bring back your coat. A website does the same thing -
it gives your browser a small ticket so it can recognize you later.

Cookies are how sites remember things like staying logged in, what is in
your shopping cart, and your preferences. Some cookies also track your
activity across visits for advertising, which is why sites ask for your
consent."""

if __name__ == "__main__":
    print(ANSWER)
