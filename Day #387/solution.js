// Open-ended: explain API vs SDK to a non-technical person. Prints the answer.
const answer = `API vs SDK (explained simply):

An API (Application Programming Interface) is like a restaurant menu.
It lists the requests you are allowed to make and what you get back.
You ask for something the way the menu specifies, and the kitchen
(another company's software) does the work and hands you the result.
You never see how the kitchen works inside.

An SDK (Software Development Kit) is like a meal-kit box with the
ingredients, recipes, and tools already included. It is a bundle of
ready-made code, examples, and helper tools that make building software
for a particular platform much faster. An SDK often contains APIs inside.

In short: an API is the "what you can ask for" contract; an SDK is the
"box of tools and shortcuts" that helps you build, and usually wraps APIs
to make them easier to use.`;

